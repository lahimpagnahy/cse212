    public static double[] MultiplesOf(double number, int length)
    {
        // Plan:
        // 1. Create a new array sized to hold "length" values.
        // 2. Loop from 0 to length - 1.
        // 3. Each multiple = number * (loop index + 1).
        //    (index 0 -> 1st multiple, index 1 -> 2nd multiple, etc.)
        // 4. Store the multiple in the array at the current index.
        // 5. Return the completed array.

        double[] multiples = new double[length];

        for (int i = 0; i < length; i++)
        {
            multiples[i] = number * (i + 1);
        }

        return multiples;
    }

